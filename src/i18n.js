const LangCtx = createContext({ lang: 'uk', t: translations.uk, setLang: () => {} });

export const LanguageProvider = ({ children }) => {
  const [lang, setLangState] = useState('uk');
  const setLang = useCallback((l) => {
    if (translations[l]) setLangState(l);
  }, []);
  return (
    <LangCtx.Provider value={{ lang, t: translations[lang], setLang }}>
      {children}
    </LangCtx.Provider>
  );
};

export const useI18n = () => useContext(LangCtx);
